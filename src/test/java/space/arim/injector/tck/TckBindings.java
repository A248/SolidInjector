package space.arim.injector.tck;

import javax.inject.Named;

import org.atinject.tck.auto.Car;
import org.atinject.tck.auto.Convertible;
import org.atinject.tck.auto.Drivers;
import org.atinject.tck.auto.DriversSeat;
import org.atinject.tck.auto.Engine;
import org.atinject.tck.auto.Seat;
import org.atinject.tck.auto.Tire;
import org.atinject.tck.auto.V8Engine;
import org.atinject.tck.auto.accessories.SpareTire;

public class TckBindings {

	public Car car(Convertible convertible) {
		return convertible;
	}

	@Drivers
	public Seat drivers(DriversSeat driversSeat) {
		return driversSeat;
	}

	public Engine engine(V8Engine engine) {
		return engine;
	}

	@Named("spare")
	public Tire tire(SpareTire spare) {
		return spare;
	}
}
