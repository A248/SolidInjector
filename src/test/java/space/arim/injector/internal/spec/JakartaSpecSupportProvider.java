public class JakartaSpecSupportProvider implements ArgumentsProvider {

	@Override
	public Stream<? extends Arguments> provideArguments(ExtensionContext context) throws Exception {
		return Stream.of(new JakartaSupport(), new CombinedSpecSupport()).map(Arguments::of);
	}

}
